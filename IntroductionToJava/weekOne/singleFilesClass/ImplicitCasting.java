//Implicit Casting
//Depending on the context, Java will implicitily cast types for us. For example:

class ImplicitCasting {
    public static void main(String[] args) {
        int i = 35;
        float f = i;
        System.out.println("This is now coverted to float: " + f);

    }
}

//This is now coverted to float: 35.0